#include "3-calc.h"

/**
 * op_add - add integers
 * @a: int 1
 * @b: int 2
 *
 * Return: addition of the two
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract integers
 * @a: int 1
 * @b: int 2
 *
 * Return: substraction of two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: multiplication of two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: num 1
 * @b: num 2
 *
 * Return: division of two num
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of two numbers
 * @a: num 1
 * @b: num 2
 *
 * Return: remainder of divion of two number
 */
int op_mod(int a, int b)
{
	return (a % b);
}
